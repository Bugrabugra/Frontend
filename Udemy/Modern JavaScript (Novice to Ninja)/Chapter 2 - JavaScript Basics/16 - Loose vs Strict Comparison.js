let age = 25;

// Loose comparison (different types can still be equal)
console.log(age == 25);
console.log(age == "25");
console.log(age != 25);
console.log(age != "25");

// Strict comparison (different types can not be equal)
console.log(age === 25);
console.log(age === "25");
console.log(age !== 25);
console.log(age !== "25");

